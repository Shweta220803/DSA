package HashMap;

public class Hashing1 {
    public static void main(String[] args) {
        int arr[] ={12, 15, 18};
        // Method - 01
        for(int i = 0; i < 3; i++){
            System.out.print(arr[i] + " ");
        }
        System.out.println();


        // Method - 02
        for(int val : arr){
            System.out.print(val + " ");
        }
        System.out.println();

    }
    
}
